package August;

public class EvenSumall 
{
	public static void main(String []args)
	{
		int sum=0;
		for(int i=1; i<20; i++)
		{
			if(i%2==0)
			{
				
				System.out.println("even number");
			}
			else
			{
				
				System.out.println("odd number");
			}
			sum=sum+i;
			{
				System.out.println(sum);
			}
			
		}
	}

}
